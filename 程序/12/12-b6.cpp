#include <iostream>
using namespace std;

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
void f_sub(int x)
{
	cout << "x = " << x << endl;
}

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
void (*f_func(const char* p))(int x) 
{
	// 打印字符串 p
	cout << "str = " << p << endl;
	// 返回指向 f_sub 的指针
	return f_sub;
}

/***************************************************************************
  函数名称：
  功    能：
  输入参数：
  返 回 值：
  说    明：
***************************************************************************/
int main()
{
	void(*(*f[3])(const char* p))(int x) = { f_func, nullptr, nullptr };

	f[0]("Hello")(2);
	f[0]("Tongji")(7);

	return 0;
}